// fazer um menu de streaming - switch e case

let streaming = "L"

switch (streaming) {

case "A" :
    console.log ("Amazon Prime");
    break;

case "N" :
    console.log ("Netflix");
    break;

case "H" :
    console.log ("HBO Max");
    break;

case "D" :
    console.log ("Disney Plus");
    break;

    default :
    console.log ("Opção inválida");
}
