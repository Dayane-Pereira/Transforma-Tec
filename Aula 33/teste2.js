/* Switch-case */

let opção = "S";

switch (opção) {
    case "S" :
        console.log ("Refrigerante");
        break;

    case "D":
        console.log ("Drink");
        break;

    case "A":
        console.log ("Água");
        break;

    case "S" :
        console.log ("Suco natural");
        break;

        default :
        console.log ("Opção inexistente");
        break;
}